          Virtual Reality adalah teknologi yang memungkinkan pengguna untuk memasuki lingkungan 3D digital melalui headset VR. Headset ini dilengkapi dengan sensor gerakan dan layar di dalamnya yang menampilkan dunia virtual. Ketika Anda bergerak atau mengarahkan kepala, dunia di dalam game juga bergerak sesuai dengan gerakan Anda. Hal ini memungkinkan pengalaman yang lebih menyeluruh dan mendalam.
  
